    private void jBSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSacuvajActionPerformed

        naziv = jTNazivProizvoda.getText();
        cenaString = jTCena.getText();
        boja = jTBoja.getText();
        String tip = jCBTipoviProizvoda.getSelectedItem().toString();
        Proizvodjac proizvodjacCB = (Proizvodjac) jCBProizvodjac.getSelectedItem();

        if (izvrsiValidaciju()) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da želite da sačuvate proizvod?", "Dodavanje proizvoda", dialogButton);
            if (dialogResult == 0) {
                proizvod = new Proizvod(0, naziv, cena, boja, tip, proizvodjacCB);
                try {
                    KlijentKontroler.getInstance().sacuvajProizvod(proizvod);
                    FPretragaNaocara fp = (FPretragaNaocara) getParent();
                    modelTabele.ModelTabeleProizvod model = fp.vratiModel();
                    model.dodajProizvod(proizvod);

                    JOptionPane.showMessageDialog(rootPane, "Uspešno ste dodali novi proizvod!");
                } catch (IOException ex) {
                    Logger.getLogger(FProizvodi.class.getName()).log(Level.SEVERE, null, ex);
                } catch (Exception ex) {
                    Logger.getLogger(FProizvodi.class.getName()).log(Level.SEVERE, null, ex);
                }
                System.out.println("Da");
            } else {
                System.out.println("Ne");
            }
        }

    }//GEN-LAST:event_jBSacuvajActionPerformed

