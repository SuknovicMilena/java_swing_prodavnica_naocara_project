    private void popuniCBProizvodjac() {
        jCBProizvodjac.removeAllItems();
        List<Proizvodjac> proizvodjaci = KlijentKontroler.getInstance().vratiSveProizvodjace();
        for (Proizvodjac proizvodjac : proizvodjaci) {
            jCBProizvodjac.addItem(proizvodjac);
        }
    }

    private void popuniCBTipovi() {
        jCBTipoviProizvoda.removeAllItems();
        String[] tipovi = KlijentKontroler.vratiTipove();
        for (String tip : tipovi) {
            jCBTipoviProizvoda.addItem(tip);
        }

    }

    void updateProizvod(Proizvod proizvod) {

        int proizvodId = proizvod.getProizvodId();
        jTProizvodID.setText(String.valueOf(new Integer(proizvodId)));
        jTNazivProizvoda.setText(proizvod.getNazivProizvoda());
        // KAKO UBACITI DOUBLE U TEKSTUALNO POLJE !!!!!!!!!!!!!
        double cena = proizvod.getCena();
        jTCena.setText(String.valueOf(new Double(cena)));

        jTBoja.setText(proizvod.getBoja());
        jCBProizvodjac.setSelectedItem(proizvod.getProizvodjac());
        jCBTipoviProizvoda.setSelectedItem(proizvod.getTip());

        jBSacuvaj.setVisible(false);
        jBIzmeni.setVisible(true);
        jBObrisi.setVisible(true);

    }

